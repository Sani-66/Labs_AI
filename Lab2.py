# Calculator program
a = float(input("Enter first number: "))
b = float(input("Enter second number: "))
operator = input("Enter operator (Add, Sub, Multi, Div): ")
input("Select any one")

if operator == "Add":
    print("Result:", a + b)
elif operator == "Sub":
    print("Result:", a - b)
elif operator == "Multi":
    print("Result:", a * b)
elif operator == "Div":
    if b != 0:
        print("Result:", a / b)
    else:
        print("Error: Division by zero")
else:
    print("Invalid operator")

